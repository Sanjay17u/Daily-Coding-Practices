    for(let x = 1; x <= 5; x++) {
        
        for(let y = 1; y <= x; y++) {
            document.write("*")
        }
        document.write("<br/>")
    }

    document.write("<hr/>")



// *******************************************************************************
// *******************************************************************************
// *******************************************************************************

// Q21. Right Triangle

for(let x = 1; x <= 5; x++) {

    for(let z = 5; z >= x; z--) {
        document.write("&nbsp;&nbsp;")
    }

    for(let y = 1; y <= x; y++) {
        document.write("*")
    }
    document.write("<br/>")
}

document.write("<hr/>")


// *******************************************************************************
// *******************************************************************************
// *******************************************************************************


// Q22. Center Triangle

for(let x = 1; x <= 5; x++) {

    for(let y = 5; y >= x; y--) {
        document.write("&nbsp;")
    }
    for(let z = 1; z <= x; z++) {
        document.write("*")
    }
    document.write("<br/>")
}

document.write("<hr/>")


// *******************************************************************************
// *******************************************************************************
// *******************************************************************************


// Q23. Reverse Left Triangle

for(let x = 5; x >= 1; x--) {

    for(let y = 1; y <= x; y++) {
        document.write("*")
    }
    document.write("<br/>")
}

document.write("<hr/>")


// *******************************************************************************
// *******************************************************************************
// *******************************************************************************


// Q24. Reverse Right Triangle

for(let x = 5; x >= 1; x--) {

    for(let y = 5; y >= x-1; y--) {
        document.write("&nbsp;&nbsp;")
    }
    for(let z = 1; z <= x; z++) {
        document.write("*")
    }
    document.write("<br/>")
}

document.write("<hr/>")


// *******************************************************************************
// *******************************************************************************
// *******************************************************************************


// Q25. Reverse Center Triangle

for(let x = 5; x >= 1; x--) {

    for(let y = 5; y >= x-1; y--) {
        document.write("&nbsp;")
    }
    for(let z = 1; z <= x; z++) {
        document.write("*")
    }
    document.write("<br/>")
}

document.write("<hr/>")


